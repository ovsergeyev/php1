<?php

function sum($arg1, $arg2){
    $result = $arg1 + $arg2;
    return $result;
}

function diff($arg1, $arg2){
    $result = $arg1 - $arg2;
    return $result;
}

function mult($arg1, $arg2){
    $result = $arg1 * $arg2;
    return $result;
}

function division($arg1, $arg2){
    $result = $arg1 / $arg2;
    return $result;
}